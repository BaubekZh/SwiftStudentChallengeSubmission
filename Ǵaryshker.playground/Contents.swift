### Have you heard about the first human in space?
Yuri Alekseyevich Gagarin  (9 March 1934 – 27 March 1968) was a Soviet Air Forces pilot and cosmonaut who became the first human to journey into outer space, achieving a major milestone in the Space Race; his capsule, Vostok 1, completed one orbit of Earth on 12 April 1961. Gagarin became an international celebrity and was awarded many medals and titles, including Hero of the Soviet Union, his nation's highest honor.

### About flight
Vostok 1 was the first spaceflight of the Vostok program and the first crewed spaceflight in history. The Vostok 3KA space capsule was launched from Baikonur Cosmodrome on April 12, 1961, with Soviet cosmonaut Yuri Gagarin aboard, making him the first human to cross into outer space.

The orbital spaceflight consisted of a single orbit around Earth which skimmed the upper atmosphere at 169 kilometers (91 nautical miles) at its lowest point. The flight took 108 minutes from launch to landing.

### The Baikonur Cosmodrome
The Baikonur Cosmodrome (Kazakh: Baıqońyr ǵarysh aılaǵy) is a spaceport located in an area of southern Kazakhstan leased to Russia.

The Cosmodrome is the world's first and largest operational space launch facility. The spaceport is located in the desert steppe of Baikonur, about 200 kilometers (120 mi) east of the Aral Sea and north of the river Syr Darya.

Both Sputnik 1, the first artificial satellite, and Vostok 1, the first human spaceflight, was launched from Baikonur. The launchpad used for both missions was renamed Gagarin's Start in honor of Russian Soviet cosmonaut Yuri Gagarin, pilot of Vostok 1 and first human in space.
 
